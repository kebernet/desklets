    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (sourcesComboBox.getSelectedItem().equals("Add New")) {
            PhotoFeed f = new PhotoFeed();
            f.setName(txtName.getText());
            f.setBaseUrl(txtUrl.getText());
            f.setPageUrl(txtPage.getText());
            f.setQueryString(txtQuery.getText());
            f.setSourceType(PhotoFeed.SourceType.valueOf((String)sourceTypeComboBox.getSelectedItem()));
            if (jCheckBox1.isSelected())
                PhotoFeed.setSelectedFeed(f);
            PhotoFeed.feeds.add(f);
        } else {
            int selectedIndex = sourcesComboBox.getSelectedIndex();
            PhotoFeed f = PhotoFeed.feeds.get(selectedIndex);
            f.setName(txtName.getText());
            f.setBaseUrl(txtUrl.getText());
            f.setPageUrl(txtPage.getText());
            f.setQueryString(txtQuery.getText());
            f.setSourceType(PhotoFeed.SourceType.valueOf(sourceTypeComboBox.getSelectedItem().toString()));
            if (jCheckBox1.isSelected())
                PhotoFeed.setSelectedFeed(f);
        }
        setVisible(false);
        Main.busyPanel.setVisible(true);
    }//GEN-LAST:event_btnSaveActionPerformed
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (sourcesComboBox.getSelectedItem().equals("Add New") != true) {
            PhotoFeed.feeds.remove(sourcesComboBox.getSelectedIndex());
        }
        formComponentShown(null);
    }//GEN-LAST:event_jButton1ActionPerformed
    
    private void sourcesComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_sourcesComboBoxItemStateChanged
        JComboBox cb = (JComboBox)evt.getSource();
        PhotoFeed selectedFeed;
        String item = (String)evt.getItem();
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            // Populate form
            for(PhotoFeed f : PhotoFeed.feeds) {
                if (f.getName().equals(item)) {
                    txtName.setText(f.getName());
                    txtUrl.setText(f.getBaseUrl());
                    txtPage.setText(f.getPageUrl());
                    txtQuery.setText(f.getQueryString());
                    sourceTypeComboBox.setSelectedItem(f.getSourceType());
                    if (PhotoFeed.getSelectedFeed() == f) {
                        jCheckBox1.setSelected(true);
                    } else { jCheckBox1.setSelected(false); }
                    break;
                }
            }
            if (item.equals("Add New")) {
                txtName.setText("");
                txtUrl.setText("");
                txtPage.setText("");
                txtQuery.setText("");
                jCheckBox1.setSelected(false);
            }
        }
    }//GEN-LAST:event_sourcesComboBoxItemStateChanged
    
    
