    private void formComponentHidden(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentHidden
        if (PhotoFeed.getSelectedFeed() != oldSelected && PhotoFeed.getSelectedFeed() != null) {
            try {
            Main.LoadImage();
            }
            catch(Exception ex)
            {
                ex.printStackTrace();
            }
        }
        PhotoFeed.saveToXML();
    }//GEN-LAST:event_formComponentHidden
    
    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        sourceTypeComboBox.removeAllItems();
        for (PhotoFeed.SourceType s: PhotoFeed.SourceType.values()) {
            
            sourceTypeComboBox.addItem(s);
        }
        sourcesComboBox.removeAllItems();
        for (PhotoFeed f: PhotoFeed.feeds) {
            sourcesComboBox.addItem(f.getName());
        }
        sourcesComboBox.addItem("Add New");
    }//GEN-LAST:event_formComponentShown
    
